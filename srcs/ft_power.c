#include "libft.h"

int				ft_power(int n, int power)
{
	int				i;
	int				res;

	if (power < 0)
		return (0);
	i = 1;
	res = 1;
	while (i <= power)
	{
		res *= n;
		i++;
	}
	return (res);
}
