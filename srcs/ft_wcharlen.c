#include "libft.h"

size_t			ft_wcharlen(wchar_t c)
{
	if (c <= 0x7FF)
		return ((c <= 0x7F) ? 1 : 2);
	else if (c <= 0x10FFFF)
		return ((c <= 0xFFFF) ? 3 : 4);
	return (0);
}
