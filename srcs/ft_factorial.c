#include "libft_basic.h"

int				ft_factorial(int n)
{
	int				i;
	int				res;

	if ((n < 0) || (n > 12))
		return (0);
	i = 2;
	res = 1;
	while (i <= n)
		res *= i++;
	return (res);
}
