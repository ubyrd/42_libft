#include "libft.h"

void			*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	unsigned char	*d;
	unsigned char	*s;

	d = (unsigned char *)dst;
	s = (unsigned char *)src;
	while (n--)
	{
		*(d++) = *(s++);
		if (*(s - 1) == (unsigned char)c)
			return ((void *)d);
	}
	return (NULL);
}
