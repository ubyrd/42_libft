#include "libft_basic.h"

int				ft_gcd(int a, int b)
{
	int				r;

	a = a >= 0 ? a : -a;
	b = b >= 0 ? b : -b;
	r = a;
	while (r)
	{
		a = b;
		b = r;
		r = a;
		while (b <= r)
			r = r - b;
	}
	return (!a ? a : b);
}
